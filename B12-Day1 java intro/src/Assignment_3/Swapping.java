package Assignment_3;

public class Swapping {

	public static void main(String[] args) {
		int x=10,y=20,temp;
		System.out.println("Before Swapping");
		System.out.println(x);
		System.out.println(y);
		temp=x;
		x=y;
		y=temp;
		System.out.println("After Swapping");
		System.out.println(x);
		System.out.println(y);

	}

}
